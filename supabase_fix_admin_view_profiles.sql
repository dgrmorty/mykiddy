-- Удаляем старую политику, если есть
DROP POLICY IF EXISTS "Admins can view all profiles" ON profiles;

-- Создаем политику для админов: они могут видеть все профили
CREATE POLICY "Admins can view all profiles"
  ON profiles FOR SELECT
  USING (is_admin_user());

-- Также убеждаемся, что админы могут видеть все профили через общую политику
-- (политика "Authenticated users can view profiles" уже должна работать, но для надежности добавим админскую)

